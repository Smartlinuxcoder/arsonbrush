void HAL_UART_RxCpltCallback(UART_HandleTypeDef *huart)
{
  if (huart->Instance == USART1)
  {
    if (rx_index == 0)
    {
      if (rx_byte == 'H')
      {
        rx_index = 1;
      }
    }
    else if (rx_index == 1)
    {
      if (rx_byte == 'i')
      {
        uint8_t msg[] = "Hello";
        HAL_UART_Transmit(&huart1, msg, 5, 100);
        rx_index = 0;
      }
      else if (rx_byte == 'H')
      {
        rx_index = 1;
      }
      else
      {
        rx_index = 0;
      }
    }
    HAL_UART_Receive_IT(&huart1, &rx_byte, 1);
  }
}
